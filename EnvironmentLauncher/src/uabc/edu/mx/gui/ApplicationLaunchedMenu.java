    @Override
    public void messageReciever(Message msg) {

        switch (msg.getType()) {

            case MESSAGE_START_APP_TO_SHOW:
                startBttn.setEnabled(false);
                pauseResumeBttn.setEnabled(true);
                detenerBttn.setEnabled(true);
                statusLbl.setText("Aplicaci√≥n Iniciada");
                break;
            case MESSAGE_RESUME_APP_TO_SHOW:
                statusLbl.setText("Aplicaci√≥n Iniciada");

                break;
            case MESSAGE_STOP_APP_TO_SHOW:
                startBttn.setEnabled(false);
                detenerBttn.setEnabled(false);
                pauseResumeBttn.setEnabled(false);

                statusLbl.setText("Aplicaci√≥n Detenida");

                break;
            case MESSAGE_PAUSE_APP_TO_SHOW:
                statusLbl.setText("Aplicaci√≥n Pausada");

                break;

        }




    }
